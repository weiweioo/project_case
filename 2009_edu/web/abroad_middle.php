              甚麼是AP課程？
            </div>
            <ul class="qa-info">
              <li>
                AP課程（Advanced Placement）是大學先修課，即在高中修讀大學一年級課程。AP 考試的成績使用5分制，考生可以獲得1，2，3，4或者5分。一般三分或三分以上的成績可以在大學換取學分。
              </li>
              <li>AP成績是向大學證明學生學習和研究能力以及應付高難度大學課程能力的重要指標。美國大學已經普遍把學生在AP考試中的表現作為衡量其是否能夠勝任大學學習的依據，因此AP考試成績已經成為眾多大學錄取考慮因素中最為重要的依據之一。</li>
            </ul>
          </div>
