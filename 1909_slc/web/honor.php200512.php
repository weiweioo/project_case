          <p>安全認證優質企業(Authorized Economic Operator，AEO)是一個訴求跨境貨物移動安全的認證制度。AEO正在國際運輸相關業界發酵，通過認證可享有快速通關優惠。</p><br>
          <p>目前全球已有超過170個國家正在推動優質企業AEO認證制度。包括台灣、歐盟、美國、中國及鄰近日、韓、星、馬等超過40個國家，均已公告AEO相關制度。</p><br>
