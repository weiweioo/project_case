              甚麼是EA/ED/RA/RD
            </div>
            <ul class="qa-info">
              <li>
                ED (Early Decision提前決定)：<br>
                學生能以ED的方式申請「一間」學校，如果被錄取，就一定要去念。截止日期多為11月1日。學生通常是為了提高Dream School的錄取機會，才使用ED，須謹慎規劃，避免高分低入的情況。
              </li>
              <li>
                EA (Early Action提前行動)：<br>
                學生可以申請多所學校，被錄取之後可以自主選擇就讀哪所學校。截止日期多為11月1日。錄取機會較RD常規錄取來的高。
              </li>
              <li>
                RD (Regular Decision常規申請)：<br>
                學生可申請多所學校。一般在1月份截止，4月公布錄取結果。相比於ED跟EA，申請RD的學生有更多時間充分準備。
              </li>
              <li>
                RA (Rolling Admission隨到隨審)：<br>
                學生可申請多所學校，沒有申請截止日期。學校在尚未招生額滿的情況下，持續接受申請。
              </li>
            </ul> 
