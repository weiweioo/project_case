          <p>
            本公司關係企業-山隆船務報關股份有限公司，於2013年首次通過AEO認證以來，受到海關及客戶大力推崇及信任。因此，為降低供應鏈管理風險，提高公司形象與客戶信賴，自2015年年初開始，由山隆通運、山隆船務報關-台中分公司、山隆船務報關-高雄分公司等三家公司，一同啟動AEO認證，歷經前期規劃、啟始會議辦理、各項制度建立、教育訓練實施等各項作業流程 <br> 最後在2016年6月順利通過書面審查與實地驗證，取得AEO認證證書，並且在不斷精益求精及持續改善之下，分別於2019年4、6月，順利通過三年校正換證作業，持續維持高標準之供應鏈安全。
