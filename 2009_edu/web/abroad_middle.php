              美國中學的分類
            </div>
            <ul class="qa-info">
              <li>美國中學分為公立與私立。</li>
              <li>美國公立中學只給持有合法居留身分的當地居民免費就讀。如果是國際學生，僅能以「交換學生」方式就讀，最長的交換時間為一年。</li>
              <li>美國私立中學，分為 「Boarding School 寄宿中學」以及「Day School走讀/日間中學」。</li>
              <li>Boarding School 寄宿中學：<br>
                學校提供校內宿舍，24小時學校人員關照。有門禁卡、出門結伴計畫、手機App追蹤，確保每位學生在當地安全學習、生活。
              </li>
              <li>Day School走讀/日間中學：<br>
                學校沒有提供校內宿舍，學生在下課後，生活起居由美國寄宿家庭來負責。走讀/日間中學的學生，比起念寄宿中學的學生，每年可節省1-2萬美元的留學費用。
              </li>
            </ul>
